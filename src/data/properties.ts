// Helper functions for data access
export function getPropertyById(id: string): Property | undefined {
  return properties.find(p => p.id === id);
}

export function getPropertyBySlug(slug: string): Property | undefined {
  return properties.find(p => p.slug === slug);
}

export function getFeaturedProperties(): Property[] {
  return properties.filter(p => p.isFeatured);
}

export function getHotDeals(): Property[] {
  return properties.filter(p => p.isHotDeal);
}

export function getLuxuryProperties(): Property[] {
  return properties.filter(p => p.isLuxury);
}

export function getLiveAuctions(): Property[] {
  return properties.filter(p => p.auctionStatus === 'Live');
}

export function getUpcomingAuctions(): Property[] {
  return properties.filter(p => p.auctionStatus === 'Upcoming');
}

export function getPropertiesByCity(city: string): Property[] {
  return properties.filter(p => p.location.city.toLowerCase() === city.toLowerCase());
}

export function getPropertiesByType(type: string): Property[] {
  return properties.filter(p => p.propertyType === type);
}

export function searchProperties(query: string): Property[] {
  const q = query.toLowerCase();
  return properties.filter(
    p =>
      p.title.toLowerCase().includes(q) ||
      p.location.city.toLowerCase().includes(q) ||
      p.location.locality.toLowerCase().includes(q) ||
      p.propertyType.toLowerCase().includes(q) ||
      p.subType.toLowerCase().includes(q) ||
      p.bankName.toLowerCase().includes(q)
  );
}

export function getTopInvestments(): Property[] {
  return [...properties].sort((a, b) => b.investmentScore.overall - a.investmentScore.overall).slice(0, 6);
}

export function getHighestROI(): Property[] {
  return [...properties].sort((a, b) => b.expectedROI - a.expectedROI).slice(0, 6);
}

export function getBestDiscounts(): Property[] {
  return [...properties].sort((a, b) => b.savingsPercent - a.savingsPercent).slice(0, 6);
}
